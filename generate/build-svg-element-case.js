export default function buildSvgElementCase({ svgElements }) {
  const entries = svgElements
    .map(el => el.tag)
    .filter(name => name !== name.toLowerCase())
    .map(name => `  ['${name.toLowerCase()}', '${name}'],`)
    .join('\n');

  return `// Generated by generate/bin/write-code-files.js — do not edit directly.\nexport const SVG_ELEMENT_CASE = new Map([\n${entries}\n]);\n`;
}
