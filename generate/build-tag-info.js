// Emits esm/tag-info.js. Maps tag method name to a tag type code, or to [code, actual tag name]
// when the tag name differs from the method name. Used by the slim Proxy-based Kensington.
const TYPE_CODES = {
  Content: 'C',
  Void: 'V',
  SvgContent: 'S',
  Math: 'M',
  LiteralContent: 'L',
  DocType: 'D',
};

function asKey(name) {
  return /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

function entryValue(el) {
  const code = TYPE_CODES[el.tagType];
  // Omit the tag name when it equals the method name (the common case).
  if (el.methodName === el.tag) {
    return code;
  }
  return `[${code}, '${el.tag}']`;
}

export default function buildTagInfo({ elements }) {
  const sorted = [...elements].sort((a, b) => a.methodName.localeCompare(b.methodName));
  const lines = sorted.map(el => `  ${asKey(el.methodName)}: ${entryValue(el)},`);
  // htmlWithDocType is the one special-case method on the full Kensington class that uses
  // HtmlWithDoctypeTag rather than ContentTag. The slim build resolves it via a dedicated
  // tag type so the Proxy dispatch can pick the right class.
  lines.push(`  htmlWithDocType: [D, 'html'],`);
  // Each code is its own value (`const C = 'C'`). The constant gives a shorter identifier
  // (`a: C` rather than `a: 'C'`) without changing what the slim Proxy receives at runtime.
  const codeDecls = Object.values(TYPE_CODES).map(c => `const ${c} = '${c}';`).join('\n');
  return `// Generated by generate/build-tag-info.js. Do not edit by hand.
// Maps tag method name to a tag type code, or to [code, actual tag name] when the tag name
// differs from the method name. Used by the slim Proxy build.
${codeDecls}

export default {
${lines.join('\n')}
};
`;
}
